# List Data Type
my_list = [1,2,3,4,5]
print("The list is:", my_list)
print("The first element of the list is:", my_list[0])
print("The last element of the list is:", my_list[-1])
my_list.append(6)
print("The list after appending 6 is:", my_list)
my_list.remove(3)
print("The list after removing 3 is:", my_list)
my_list[1] = 10
print("The list after changing the second element to 10 is:", my_list)